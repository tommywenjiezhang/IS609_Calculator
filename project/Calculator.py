import math
class Calculator:
    def add(self,x,y):
        return x+y
    def minus(self,x,y):
        return y-x
    def multiply(self,x,y):
        return x*y
    def divide(self,x,y):
        return x/y
    def square(self,x):
        return x**2
    def sqrt(self,x):
        return math.sqrt(x)
    